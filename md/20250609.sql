## 📘 **1. 주간/월간 업무 보고서 템플릿**

> 📅 보고서 유형: 주간 또는 월간
> 📌 사용 대상: 개발자, 팀 리더, PM

```
[업무 보고서 - 주간/월간]

작성자: [이름]  
작성일: [YYYY-MM-DD]  
보고 기간: [예: 2025-06-02 ~ 2025-06-06]  
소속 팀/부서: [예: 플랫폼개발팀]

1. 📌 **요약 (Summary)**
- [이번 주/월에 수행한 핵심 업무 요약 (3줄 이내)]

2. ✅ **진행 업무**
| 업무 항목 | 상세 내용 | 완료율 | 비고 |
|-----------|-----------|--------|------|
| 예: 로그인 API 개발 | Spring Security 적용, 테스트 완료 | 100% | 배포 대기 |
| 예: UI 개선 작업 | 피드백 반영 중 | 60% | 디자이너 협의 필요 |

3. ⚠️ **이슈 및 리스크**
| 이슈 내용 | 발생일 | 영향 범위 | 조치 계획 |
|-----------|--------|-------------|-------------|
| 서버 다운 현상 | 06/04 | QA 서버 | 서버 자원 모니터링 추가 예정 |
| APM 누락 | 06/05 | 전체 모듈 | NewRelic 연동 작업 진행 중 |

4. 🔄 **다음 계획 (Next Plan)**
- [예: 배포 자동화 구현, QA 피드백 반영, 문서화 등 구체적으로 작성]

5. 📎 **기타 공유 사항**
- [회의 일정, 팀 협업 관련 공지 등]
```

