## 🛠️ **2. 이슈 리포트 템플릿**

> 📌 사용 대상: QA, 개발자, 운영팀
> 🎯 목적: 버그/장애 발생 시 정확한 기록과 대응

```
[이슈 리포트]

이슈 번호: #[YYYYMMDD-01]  
작성자: [이름]  
발생 일시: [YYYY-MM-DD HH:mm:ss]  
발생 환경: [운영/QA/개발]  
시스템: [모듈 또는 서비스 이름]  
버전: [릴리즈 버전 또는 브랜치 명]

1. 🧩 **이슈 개요**
- [한 줄로 요약된 이슈 설명, 예: 로그인 시도 시 500 에러 발생]

2. 📋 **상세 증상**
- [사용자 행동 및 오류 상황 설명]
- [스크린샷 또는 로그 예시 첨부]
- 에러 메시지: `[java.lang.NullPointerException at ~]`

3. 🔍 **재현 절차**
1. [예: 로그인 화면 접속]
2. [이메일/비밀번호 입력]
3. [로그인 버튼 클릭]

4. 🧠 **원인 분석**
- [원인 로그 및 코드 분석 결과]
- [예: 토큰 파싱 중 null 값 발생]

5. 🛠️ **조치 내역**
- [임시 조치: API 재시작]
- [영구 조치: null 체크 로직 추가 (06/10 배포 예정)]

6. 🔒 **재발 방지 방안**
- [예: 사용자 입력 검증 강화, 모니터링 알림 연동 등]

7. 📎 **기타**
- 관련 이슈 링크: [JIRA 또는 GitHub Issue 링크]
- 담당자: [예: 홍길동]
```

