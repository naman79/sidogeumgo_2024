SELECT 
    SUNAPIL,
    GEORAE_GUBUN,
    SUM(BONSE_AMT) AS BONSE,
    SUM(SEWOI_AMT) AS SEWOI,
    SUM(GYOYUKSE_AMT) AS GYOYUKSE,
    SUM(NONGTEUKSE_AMT) AS NONGTEUKSE,
    SUM(BONSE_AMT) + SUM(SEWOI_AMT) + SUM(GYOYUKSE_AMT) + SUM(NONGTEUKSE_AMT) AS TOTAL
FROM RPT_SUNAP_JIBGYE
WHERE 1=1
AND GEUMGO_CODE = 28
AND GUNGU_CODE = 0
AND JIBGYE_CODE IN (103000, 104000)
AND SUNAPIL BETWEEN '20250101' AND '20250331'
GROUP BY SUNAPIL, GEORAE_GUBUN
ORDER BY SUNAPIL, GEORAE_GUBUN

