SELECT
    T1.GONGGEUM_GYEJWA
     ,T3.SIGUMGO_AC_NM AS GONGGEUM_GYEJWA_NM
     ,T1.GRBRNO
     ,T2.TRXDT AS GYULSAN_IL
     ,T2.FIL_DT1 AS BF_GYULSAN_IL
     ,T1.IJA
     ,DECODE(T3.SIGUMGO_ORG_C, 28, T3.ICH_SIGUMGO_HOIKYE_C, T4.SIGUMGO_HOIKYE_C) AS GONGGEUM_HOIGYE_CODE
     ,TO_NUMBER(SUBSTR(T2.TRXDT, 5, 2)) AS GYEOLSANWOL
FROM
    RPT_GONGGEUM_GYULSAN_TMP T1
   ,ACL_SIGYUL_SLV T2
   ,ACL_SIGUMGO_MAS T3
   ,RPT_AC_BY_HOIKYE_MAPP T4
WHERE 1=1
  AND T1.YUDONG_ACNO = T2.EFT_ACNO
  AND T1.GYULSAN_IL = T2.TRXDT
  AND T1.GONGGEUM_GYEJWA = T3.FIL_100_CTNT2
  AND T3.FIL_100_CTNT2 = T4.SIGUMGO_ACNO(+)
  AND T3.SIGUMGO_HOIKYE_YR = T4.SIGUMGO_HOIKYE_YR(+)
  AND T1.GEUMGO_CODE = '28'
  AND T1.GYULSAN_IL = '20240701'
  AND T1.IJA <> 0
  AND DECODE(NVL('50', '9999'), '9999', 1, DECODE(T1.GUNGU_CODE, '50', 1, 0)) = 1
  AND DECODE(NVL('', '9999'), '9999', 1, DECODE(T1.GRBRNO, '', 1, 0)) = 1
  AND DECODE(NVL('', '9999'), '9999', 1, DECODE(SUBSTR(T1.GONGGEUM_GYEJWA, 16, 2), '99', 1, SUBSTR('', 3, 2), 1, 0)) = 1
  AND T3.MNG_NO = 1
ORDER BY
    T1.GONGGEUM_GYEJWA



