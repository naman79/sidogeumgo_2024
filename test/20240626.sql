SELECT SIGUMGO_TRX_G, SIGUMGO_IP_TRX_G, SIGUMGO_JI_TRX_G ,
SUM(CASE WHEN SIGUMGO_TRX_G < 60 THEN DECODE(CRT_CAN_G, 1, -1, 2, -1, 33, -1, 1)*TRAMT ELSE 0 END) AS IP_AMT,
SUM(CASE WHEN SIGUMGO_TRX_G >= 60 THEN DECODE(CRT_CAN_G, 1, -1, 2, -1, 33, -1, 1)*TRAMT ELSE 0 END) AS JI_AMT
FROM
ACL_SIGUMGO_SLV
WHERE 1=1
AND SIGUMGO_ORG_C = 28 
AND FIL_100_CTNT5 = '02800001100000024'
AND TRXDT BETWEEN '20240101' AND '20240531'
GROUP BY SIGUMGO_TRX_G, SIGUMGO_IP_TRX_G, SIGUMGO_JI_TRX_G
ORDER by SIGUMGO_TRX_G, SIGUMGO_IP_TRX_G, SIGUMGO_JI_TRX_G

