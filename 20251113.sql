AND ICH_SIGUMGO_GUN_GU_C = 0
AND ICH_SIGUMGO_HOIKYE_C = 1
AND SIGUMGO_HOIKYE_YR = 2024
AND FIL_100_CTNT5 = '02800001100000024'
